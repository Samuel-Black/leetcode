/**
 * @param {number[][]} mat
 * @param {number} k
 * @return {number[]}
 */
 var kWeakestRows = function(mat, k) {
    let maxHeap = new MaxPriorityQueue();
    // for each row, sum row and 
    for (let i = 0; i < mat.length; i++) {
      let rowSum = 0;
      // sum each row
      for(let j = 0; j < mat[i].length; j++) {
        rowSum += mat[i][j];
      }

      // if the row sum is < current max value, remove it and insert the current row sum
      const currentMax = Math.floor(maxHeap?.front()?.priority);
      if (i < k) {
        maxHeap.enqueue(i, Math.abs(Number(`${rowSum}.${i}`)) * -1);
      } else if (rowSum < currentMax) {
        maxHeap.dequeue();
        maxHeap.enqueue(i, Math.abs(Number(`${rowSum}.${i}`)) * -1);
      }
    }

    // const resArray = [];

    // while (maxHeap.size() > 0) {
    //   resArray.unshift(maxHeap.dequeue());
    // }

    return maxHeap.toArray().map(node => {
      return node?.element;
    }).reverse();

};

