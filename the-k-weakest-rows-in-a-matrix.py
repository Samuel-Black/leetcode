from typing import List
import heapq

class Node(object):
  def __init__(self, index: int, val: int):
    self.index = index
    self.val = val

  def __repr__(self):
    return repr(self.index)

  def __lt__(self, other):
    if self.val == other.val:
      return self.index > other.index
    else:
      return self.val > other.val

class Solution:
  def kWeakestRows(self, mat: List[List[int]], k: int) -> List[int]:
    maxHeap = []
    heapq.heapify(maxHeap)
    
    for index, row in enumerate(mat):
      sum = 0
      for col in row:
        sum += col
        
      currentMax = maxHeap[0].val if maxHeap.__len__() > 0 else 0
      sum = sum
      
      if maxHeap.__len__() < k:
        heapq.heappush(maxHeap, Node(index, sum))
      elif sum < currentMax:
        heapq.heappushpop(maxHeap, Node(index, sum))

    maxHeap.reverse()
    return maxHeap        
